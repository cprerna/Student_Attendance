<h4>Platform:</h4>
<p>Web-based application developed using PHP and MySQL.</p>

<h4>Objective:</h4>
<p>The main goal of this project is to simplify the process of recording and managing 
    student attendance. It enables teachers to take daily attendance for each class and 
    generate monthly attendance reports.</p>

<h4>Scope:</h4>
<p>This system allows management of multiple classes, student lists, and records of 
    attendance status (Present/Absent) for specific dates. It helps reduce paperwork 
    and manual errors while maintaining transparency.</p>

<h4>Key Features:</h4>
<ul>
    <li>Add/Edit/Delete Classes</li>
    <li>Add/Edit/Delete Students</li>
    <li>Daily Attendance Form with present/absent checkboxes</li>
    <li>"Check All/Uncheck All" options for quick marking</li>
    <li>Prevents submission if any student's attendance is not marked</li>
    <li>Monthly attendance report generation per class</li>
</ul>

<h4>Technologies Used:</h4>
<ul>
    <li>Frontend: HTML, CSS, Bootstrap 5</li>
    <li>Backend: PHP 8.2</li>
    <li>Database: MySQL</li>
    <li>Tools: XAMPP/WAMP (for Initial local deployment)</li>
</ul>

<h4>Modules:</h4>
<ol>
    <li>Class Management - Manage class/course names.</li>
    <li>Student Management - Register students under specific classes.</li>
    <li>Attendance Management - Take attendance daily for each class.</li>
    <li>Reports Module - View and generate attendance reports monthly.</li>
</ol>

<h4>How It Works:</h4>
<ul>
    <li>Admin adds classes and students.</li>
    <li>For each class, attendance is marked by selecting the date and choosing present/absent for each student.</li>
    <li>Records are saved in the database.</li>
    <li>Attendance can be reviewed and summarized through a reporting interface.</li>
</ul>

<h4>Benefits:</h4>
<ul>
    <li>Reduces manual workload</li>
    <li>Provides organized attendance records</li>
    <li>Can be easily modified for institutional requirements</li>
</ul>

